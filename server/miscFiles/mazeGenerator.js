    runTrial() {
      try {
        switch (this.type) {
            case 0:
                this.runNormal()
                break
            case 1:
                this.runNormal2();
                break;
            case 2:
                this.run2Teams()
                break
            case 4:
                this.run4Teams()
                break
            case 8:
                this.run8Teams()
                break
            case 9:
                this.runMothership()
                break;
            case 10:
                this.runBunker()
                break
            case 11:
                this.runFortress()
                break
            case 12:
                this.runCitadel()
                break
            case 13:
                this.runBlitz()
                this.hasToBeClosed = true;
                break;
            case 14:
                this.runSiegeOld();
                break;
            case 15:
                this.runBooster();
                break;
            case 16:
                this.runTrenches();
                break;
            case 17:
                this.runLine();
                break;
            case 18:
                this.runEye();
                break;
            case 19:
                this.runAcropolis();
                break;
            case 20:
                this.runYinYang();
                break;
            case 80:
                this.run8TeamsLabyrinth()
                this.hasToBeClosed = true;
                break
            default:
                console.log("Invalid maze type! Generating aborded.")
                return null
                break
        }
      } catch (e) {
        return null
      }
      if (this.isClosed()) {
        if (!this.hasToBeClosed) {
            console.error("Generating maze failed, reattemping to regenerate the maze...");
            return null
        }
      }
      return new MazeZone(this.maze)
    }
    placeMinimal() {
      let bestSquares = null
  
      for (let i = 0; i < 10; i++) {
        let trial = this.runTrial()
        if (!trial) continue
        let squares = trial.intoSquares()
        if (bestSquares === null || squares.length < bestSquares.length) {
          bestSquares = squares
          continue
        }
      }
  
      for (let i = 0; !bestSquares && i < 500; i++) {
        let trial = this.runTrial()
        if (!trial) continue
        bestSquares = trial.intoSquares()
      }
  
      return {
        squares: bestSquares,
        width: this.width,
        height: this.height,
      }
    }
